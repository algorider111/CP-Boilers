// headers_list.cpp
// List of commonly used C++ standard headers with short descriptions

#include <algorithm>     // sort, reverse, min, max, binary_search
#include <array>         // fixed-size array (std::array)
#include <bitset>        // bitset
#include <cassert>       // assert
#include <cctype>        // isdigit, isalpha, tolower, toupper
#include <cerrno>        // error codes (errno)
#include <climits>       // INT_MAX, INT_MIN
#include <cmath>         // sqrt, pow, abs, ceil, floor
#include <complex>       // complex numbers
#include <csignal>       // signal handling
#include <cstdarg>       // variable argument lists
#include <cstddef>       // size_t, ptrdiff_t
#include <cstdint>       // fixed-width integer types (int64_t etc.)
#include <cstdio>        // printf, scanf, FILE*
#include <cstdlib>       // malloc, free, exit, rand, abs
#include <cstring>       // memset, memcpy, strlen, strcmp
#include <ctime>         // time, clock, difftime
#include <chrono>        // time utilities (high_resolution_clock etc.)
#include <random>        // random number generators
#include <deque>         // deque
#include <exception>     // exception base class
#include <fstream>       // file input/output
#include <functional>    // function objects, std::greater
#include <iomanip>       // setprecision, fixed
#include <ios>           // stream formatting base
#include <iosfwd>        // forward declarations of I/O classes
#include <iostream>      // cin, cout
#include <iterator>      // iterators
#include <limits>        // numeric_limits
#include <list>          // doubly linked list
#include <locale>        // localization
#include <map>           // map, multimap
#include <memory>        // smart pointers (unique_ptr, shared_ptr)
#include <new>           // placement new
#include <numeric>       // accumulate, gcd, lcm
#include <optional>      // std::optional (C++17)
#include <queue>         // queue, priority_queue
#include <set>           // set, multiset
#include <stack>         // stack
#include <stdexcept>     // runtime_error, invalid_argument, etc.
#include <string>        // string, substr, getline
#include <string_view>   // string_view (C++17)
#include <system_error>  // error_code
#include <thread>        // multithreading
#include <tuple>         // tuple
#include <type_traits>   // type checking at compile time
#include <unordered_map> // unordered_map (hash map)
#include <unordered_set> // unordered_set
#include <utility>       // pair, move, swap
#include <valarray>      // numeric array
#include <variant>       // std::variant (C++17)
#include <vector>        // vector

